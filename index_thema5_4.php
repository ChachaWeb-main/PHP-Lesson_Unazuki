<?php
/* --------------------------------------------
 * 　多次元配列
 WordPressでは多次元配列が多用されます。
 これを読みとけるようになるとコードの理解がかなり進みますので理解しましょう
 言葉は難しく感じますが配列を理解していればそんなに難しくありません
