<!-- 1．繰り返し構文 forを学ぼう -->

<?php
/* --------------------------------------------
 * 　for文とは
 * -------------------------------------------- */

for ($i = 1; $i <= 10; $i++) {
    echo $i . '<br>';
}
echo '<br><br>';

for ($i = 1; $i <= 10; $i++) {
    echo $i . '<br>';
    if ($i == 5) {
        break;
    }
}
echo '<br><br>';

for ($i = 1;; $i++) {
    echo $i . '<br>';
    if ($i == 5) {
        break;
    }
}
?>
