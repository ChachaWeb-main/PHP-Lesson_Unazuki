<?php
/* --------------------------------------------
 * 　値の存在を確認する条件分岐
* -------------------------------------------- */
$hello = 'こんにちは';
if ($hello) {
    echo $hello;
}

/* --------------------------------------------
 * 　値の存在を確認する条件分岐
* -------------------------------------------- */
$hello = '';
if ($hello) {
	echo 'hello変数が空の文字列なら条件式はfalseを返すので何も表示されない';
}
?>
